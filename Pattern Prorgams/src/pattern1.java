public class pattern1 
{

	public static void main(String[] args)
	{
		int n = 5;
		
		for (int row = 1 ; row <= n ; row++) //outer loop
		{
			for (int column = 1 ; column <= n; column++)
			{
				System.out.print("*");
			}
			System.out.println();
		}

	}

}
