package operators;

public class Casting 
{
	public static void main(String[] args)
	{
		// implicit casting done by automaticallt
		
		double d1 =4;       //int to double
		double d2 =5.7f;    // float to double
		long l1 =100;		// int to long
		
		
		//explicit casting done by programmer using paranthesis
		
		int i1= (int) 4.5;
		System.out.print(i1); //op = 4
		
	}
}
